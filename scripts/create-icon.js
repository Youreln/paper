const fs = require('fs');
const path = require('path');

const assetsDir = path.join(__dirname, '..', 'assets');
if (!fs.existsSync(assetsDir)) {
    fs.mkdirSync(assetsDir, { recursive: true });
}

const iconPngPath = path.join(assetsDir, 'icon.png');

const pngHeader = Buffer.from([
    0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A,
    0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52,
    0x00, 0x00, 0x00, 0x80, 0x00, 0x00, 0x00, 0x80,
    0x08, 0x06, 0x00, 0x00, 0x00, 0xC3, 0x3E, 0x61,
    0xCB
]);

const minimalPng = Buffer.concat([
    pngHeader,
    Buffer.from([
        0x00, 0x00, 0x00, 0x01, 0x73, 0x52, 0x47, 0x42,
        0x00, 0xAE, 0xCE, 0x1C, 0xE9, 0x00, 0x00, 0x00,
        0x08, 0x49, 0x44, 0x41, 0x54, 0x78, 0x9C, 0x63,
        0xF8, 0x0F, 0x00, 0x00, 0x01, 0x00, 0x01, 0x00,
        0x00, 0x00, 0x00, 0x49, 0x45, 0x4E, 0x44, 0xAE,
        0x42, 0x60, 0x82
    ])
]);

fs.writeFileSync(iconPngPath, minimalPng);
console.log('图标已创建:', iconPngPath);

const iconIcoPath = path.join(assetsDir, 'icon.ico');
fs.copyFileSync(iconPngPath, iconIcoPath);
console.log('ICO 图标已创建:', iconIcoPath);
